// 1. Arithmetic Operators

let x;

x = 5 + 5;
x = 5 - 5;
x = 5 * 5;
x = 5 / 5;
x = 7 % 5; 

console.log(x);
// Concatenation
x = 'Hello' + ' ' + 'World';
console.log(x);

// Exponent
x = 2 ** 3;

// Increment
x = 1;
// x = x + 1;
x++;

// Decrement
// x = x - 1;
x--;


// 2. Assignment Operators
x = 10;

x += 5;
x -= 5;
x *= 5;
x /= 5;
x %= 5;
x **= 5;
console.log(x);

// 3. Comparison Operators
//  Equal to (Just the value, not the type)

x = 2 == '2';
console.log(x);

// Equal to (Type and value)

x = 2 === '2';
console.log(x);

// Not equal to (Just the value, not the type)

x = 2 != '2';
console.log(x);

// Not equal to (Type and value)

x = 2 !== 2;
console.log(x);

// Greater than and less than

x = 10 > 5;
console.log(x);

x = 10 < 5;
console.log(x);

x = 10 <= 5;
console.log(x);

x = 10 >= 5;
console.log(x);

